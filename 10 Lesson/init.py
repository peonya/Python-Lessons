
def sum(a,b):
    sum = a + b
    print(a, '+', b, "=",sum)
    return sum

def diff(a,b):
    diff = a - b
    print(a, '-', b, "=", diff)
    return diff

def mult(a,b):
    mult = a * b
    print(a, '*', b, "=", mult)
    return mult

def div(a,b):
    div = a / b
    print(a, '/', b, "=", div)
    return div